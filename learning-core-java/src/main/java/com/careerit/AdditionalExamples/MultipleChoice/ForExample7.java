package com.careerit.AdditionalExamples.MultipleChoice;

public class ForExample7
{
    public static void main(String[] args)
    {
        int k=1;
        for(int i=1;i<3;i++)
        {
            for(int j=1;j<=3;j++)
            {
                System.out.print(k+++" ");
            }
            System.out.println();
        }
    }
}
