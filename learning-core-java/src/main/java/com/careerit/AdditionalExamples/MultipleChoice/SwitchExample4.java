package com.careerit.AdditionalExamples.MultipleChoice;

public class SwitchExample4
{
    public static void main(String[] args)
    {
        for (int i = 3; i >= 0; --i)
        {
            switch (i)
            {
                case 1:
                    System.out.println(i);
                    break;
                case 2:
                    System.out.println(i);
                    break;
                case 3:
                    System.out.println(i);
                    break;
                default:System.out.println(i);
            }

        }
    }
}
